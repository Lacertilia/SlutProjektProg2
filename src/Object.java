public class Object {
    private int x;
    private int y;

    /**
     * Creator for object.
     */
    public Object(){

    }

    /**
     * Getter for x variable.
     * @return x value
     */
    public int getX(){
        return this.x;
    }

    /**
     * Getter for y variable.
     * @return y value
     */
    public int getY(){
        return this.y;
    }
}
